// Days of the Week program
var day = "Sunday";

switch (day) {
  case "Monday":
    console.log("Go to work");
    break;
  case "Tuesday":
    console.log("Meeting at 10 AM");
    break;
  case "Wednesday":
    console.log("Gym workout");
    break;
  case "Thursday":
    console.log("Work on the project");
    break;
  case "Friday":
    console.log("Casual Friday at the office");
    break;
  case "Saturday":
    console.log("Relax and enjoy the weekend");
    break;
  case "Sunday":
    console.log("Family time and rest");
    break;
  default:
    console.log("There is no such day");
}